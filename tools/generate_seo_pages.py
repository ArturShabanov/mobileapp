def esc(value: str) -> str:
    return html.escape(value, quote=True)


def service_nav(current_slug: str) -> str:
    links = []
    for label, url in SERVICE_LINKS:
        active = " service-nav__link--active" if current_slug in url else ""
        links.append(f'<a class="service-nav__link{active}" href="{url}">{esc(label)}</a>')
    return "\n          ".join(links)


